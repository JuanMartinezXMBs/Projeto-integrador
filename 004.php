 <p class="centralizar">Em primeiro lugar, Sola Scriptura. A Bíblia é a Palavra de Deus inspirada, inerrante, infalível e sufi ciente. É nossa única regra de fé e prática. É composta de sessenta e seis livros, escritos por homens santos de Deus. A Bíblia não é julgada pela cultura nem mesmo validada pelos concílios. A Palavra de Deus 
está acima da tradição, das confissões, dos catecismos, das decisões conciliares. Sua mensagem não precisa ser atualizada nem ressignificada. Ela é sempre atual, sempre viva, sempre poderosa, sempre eficaz. Ela é eterna. A Palavra Deus restaura a alma, ilumina os olhos e dá sabedoria aos simples. Por meio dela nascemos, crescemos e somos fortalecidos. Pela sua instrumentalidade somos santificados, exortados e consolados. Ela é leite para os infantes e carne para os experimentados. É mais preciosa do que ouro e mais doce que o mel. Ela é pão que alimenta, água que dessedenta, martelo que debulha, fogo que aquece e espada que defende. </p>

 <p class="centralizar">Em segundo lugar, Sola Gratia. A salvação é totalmente pela graça. Não somos salvos por aquilo que fazemos para Deus, mas pelo que Deus fez por nós, em Cristo Jesus. Não são nossas obras que nos levam para o céu, mas a obra de Cristo na cruz. Ele morreu pelos nossos pecados e pagou o preço da nossa redenção. A vida eterna não é uma conquista do mérito humano, mas uma oferta da graça de Deus. A vida eterna não é uma medalha de honra ao mérito que ostentamos, mas o presente do Rei recebido por um mendigo. Somos salvos totalmente pela graça para que toda a glória seja dada a Deus. </p>

 <p class="centralizar">Em terceiro lugar, Sola Fide. A salvação não é pelas obras nem pela fé mais as obras, mas pela graça, mediante a fé, para as obras. Somos salvos não pelas obras, mas para as obras. As boas obras não são a causa da nossa salvação, mas a sua evidência. Não fazemos boas obras para sermos salvos, mas porque fomos salvos pela fé em Cristo Jesus. Fé e obras não estão em conflito. Uma é causa, a outra a consequência. Onde tem fé como raiz, há obras como fruto. Mas não é fé nos ídolos nem fé na fé, mas fé em Cristo Jesus. A fé não é causa meritória da nossa salvação; é a causa instrumental. </p>

